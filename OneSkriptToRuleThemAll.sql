  )
     
CREATE TABLE Посади (
   ID_Посади int PRIMARY KEY,
   Повна_назва_посади varchar (100) not null,
   Опис_роботи text not null
   )

CREATE TABLE Персонал (
  ID_Працівника varchar (100) PRIMARY KEY,
  Прізвище varchar (100) not null,
  Імя varchar (100) not null, 
  Побатькові varchar (100) not null, 
  Дата_народження datetime not null,
  Стать varchar (100) not null,
  Сімейний_стан varchar (100) not null,
  Серія_Номер_Паспорта varchar (100) not null,
  ID_Посади int FOREIGN KEY REFERENCES Посади (ID_Посади),
  Дата_початку_роботи datetime not null,
  Коментарі text,
 CONSTRAINT Йдемо_у_Європу CHECK (Стать IN ('Жінка', 'Чоловік', 'Інше')),
  CONSTRAINT Унік_паспорт UNIQUE (Серія_Номер_Паспорта),
  CHECK (Сімейний_стан <> 'Все складно')
  )

  CREATE TABLE Аеропорти (
  IATA_Code varchar (100) PRIMARY KEY,
  Назва_аеропорту varchar (100) UNIQUE,
  Країна varchar (100) not null,
  Регіон varchar (100) not null,
  Місто varchar (100),
  Висота_над_рівнем_моря varchar (100) not null,
  Координати varchar (100) UNIQUE)

  CREATE TABLE Тип_Рейсу (
  ID_Рейсу int PRIMARY KEY,
  Дата_початку_обслуговування date not null,
  Дата_завершення_обслуговування date not null,
  Тривалість_польоту time not null,
  IATA_Code_Аеропорту_відправлення varchar (100) not null,
  IATA_Code_Аеропорту_прибуття varchar (100)not null,
  FOREIGN KEY (IATA_Code_Аеропорту_відправлення) REFERENCES Аеропорти(IATA_Code),
  FOREIGN KEY (IATA_Code_Аеропорту_прибуття) REFERENCES Аеропорти(IATA_Code),
  )

  
CREATE TABLE Політ (
 ID_Польоту int PRIMARY KEY,
 ДатаВильоту datetime not null,
 ID_Рейсу int not null,
 Літак varchar (100) not null,
